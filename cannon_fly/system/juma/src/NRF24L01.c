//写寄存器
uint8_t NRF_Write_Reg(uint8_t reg, uint8_t value)
{
    uint8_t status;
    SPI2_CSN_L();		
    HAL_Delay(1);	
    status = HAL_SPI_Transmit(&hnucleo_Spi2,&reg, 1, 100);  
	  HAL_SPI_Transmit(&hnucleo_Spi2,&value, 1, 100);
  //  SPI2_RW(value);		  /* 写数据 */
	HAL_Delay(1);	
    SPI2_CSN_H();					  /* 禁止该器件 */
    return 	status;
}


//读寄存器
uint8_t NRF_Read_Reg(uint8_t reg)
{
    uint8_t reg_val;
    SPI2_CSN_L();		
    HAL_Delay(1);		
    HAL_SPI_Transmit(&hnucleo_Spi2,   &reg, 1, 100); 
    HAL_SPI_Receive(&hnucleo_Spi2,  &reg_val, 1, 100);	  /* 读取该寄存器返回数据 */
    SPI2_CSN_H();	
 
    return 	reg_val;
}


