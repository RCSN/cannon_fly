//写数据包
void NRF_TxPacket(uint8_t * tx_buf, uint8_t len)
{	
    SPI2_CE_L();		 //StandBy I模式	
    NRF_Write_Buf(WR_TX_PLOAD, tx_buf, len); 			 // 装载数据	
    SPI2_CE_H();		 //置高CE，激发数据发送
}

